// Modificador 'public':
// Indica que o membro é acessível de qualquer lugar, dentro ou fora do mesmo assembly.
// Exemplo: membros públicos de uma classe podem ser acessados por qualquer outra classe.

// Modificador 'protected':
// Permite que o membro seja acessível apenas dentro da própria classe e por classes derivadas (subclasses).
// Não pode ser acessado fora da classe, a menos que seja uma classe derivada.

// Modificador 'internal':
// Indica que o membro é acessível apenas dentro do mesmo assembly, não podendo ser acessado fora dele.
// Pode ser acessado por qualquer classe dentro do mesmo assembly.

// Modificador 'protected internal':
// Combina os comportamentos de 'protected' e 'internal'.
// O membro é acessível dentro do mesmo assembly e por classes derivadas, mesmo que estejam em assemblies diferentes.

// Modificador 'private':
// Indica que o membro é acessível apenas dentro da própria classe.
// Não pode ser acessado por classes externas, mesmo que sejam derivadas da classe que os define.
