// Tipos de Herança em C#:

// 1. Herança Simples (Single Inheritance):
//    Neste tipo de herança, uma classe pode herdar de apenas uma classe base. Em C#, uma classe pode herdar de uma única classe direta.

// 2. Herança Múltipla (Multiple Inheritance):
//    Na herança múltipla, uma classe pode herdar de mais de uma classe base. No entanto, muitas linguagens de programação, incluindo C#, não suportam herança múltipla direta devido a possíveis problemas de ambiguidade e complexidade.

// 3. Herança Hierárquica (Hierarchical Inheritance):
//    Neste tipo de herança, várias classes derivam de uma única classe base. Por exemplo, imagine uma classe Animal com subclasses como Cachorro, Gato e Pássaro. Todas essas subclasses herdam de Animal.

// 4. Herança Híbrida (Hybrid Inheritance):
//    A herança híbrida combina características da herança simples e múltipla. Ela permite que uma classe herde de uma classe base e também implemente uma ou mais interfaces. Isso é comum em linguagens como C#.

// 5. Herança Multinível (Multilevel Inheritance):
//    Na herança multinível, as classes são organizadas em uma hierarquia em vários níveis. Por exemplo, uma classe pode herdar de outra classe que, por sua vez, herda de uma terceira classe e assim por diante.

// 6. Herança Cíclica (Cyclic Inheritance):
//    Este é um tipo de herança onde uma classe, direta ou indiretamente, herda de si mesma. Esse tipo de herança é geralmente evitado, pois pode levar a problemas de recursão infinita.

// 7. Herança de Interface (Interface Inheritance):
//    Em C# e em outras linguagens que suportam interfaces, as classes podem herdar apenas de uma classe base, mas podem implementar várias interfaces. Isso permite que as classes forneçam implementações para múltiplos contratos definidos pelas interfaces.

// Cada tipo de herança tem suas próprias características e casos de uso específicos. É importante entender os diferentes tipos de herança para projetar e modelar hierarquias de classes de forma eficaz em seus programas.
