using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace ProgamandoEmCSharp.basics.operadores
{
    public class OperadoresComparacao
    {
        // Podemos comparar qualquer tipo de dado
        // A comparação semore retorna true ou false

        int x =1;
        int y =2;


        bool result = x==y;// e igual?
        result = x !=y;// e diferente?
        result = x >y;// e maior?
        result = x <y;// e menor?
        result = x >= y;// e maior ou igual?
        result = x <=y ;// e menor ou igual?
    }
}